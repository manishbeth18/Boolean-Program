public class Boolean {
    public static void main(String[] args){
        int a=10;
        int b=12;
        int result=a&b;
        int result1=a|b;
        int result2=a^b;
        int result3=~b;
        System.out.println("a&b="+result);
        System.out.println("a|b="+result1);
        System.out.println("a^b="+result2);
        System.out.println("~b="+result3);
        

    }

}
